import '../services/api_service.dart';

/// A teacher's AI avatar (photo + cloned voice), pending admin approval.
class TeacherAvatar {
  final int id;
  final int teacherId;
  final String? displayName;
  final String? photoUrl;
  final String? voiceSampleUrl;
  final String status; // pending | approved | rejected
  final String? rejectionReason;
  final String? voiceCloneWarning;
  final String? reviewedAt;
  final String createdAt;

  TeacherAvatar({
    required this.id,
    required this.teacherId,
    this.displayName,
    this.photoUrl,
    this.voiceSampleUrl,
    required this.status,
    this.rejectionReason,
    this.voiceCloneWarning,
    this.reviewedAt,
    required this.createdAt,
  });

  bool get isPending => status == 'pending';
  bool get isApproved => status == 'approved';
  bool get isRejected => status == 'rejected';

  factory TeacherAvatar.fromJson(Map<String, dynamic> json, ApiService api) {
    String? fixed(dynamic v) => (v == null || v.toString().isEmpty) ? null : api.fixUrl(v.toString());
    return TeacherAvatar(
      id: (json['id'] as num).toInt(),
      teacherId: (json['teacher_id'] as num?)?.toInt() ?? 0,
      displayName: json['display_name'] as String?,
      photoUrl: fixed(json['photo_url']),
      voiceSampleUrl: fixed(json['voice_sample_url']),
      status: json['status'] as String? ?? 'pending',
      rejectionReason: json['rejection_reason'] as String?,
      voiceCloneWarning: json['voice_clone_warning'] as String?,
      reviewedAt: json['reviewed_at'] as String?,
      createdAt: json['created_at'] as String? ?? '',
    );
  }
}

/// A lecture generated by a teacher's AI avatar from an uploaded presentation.
class AvatarLecture {
  final int id;
  final int avatarId;
  final int classId;
  final int createdBy;
  final String title;
  final String? sourceFilename;
  final int durationMinutes;
  final String style; // school | university | professional
  final bool autoSummary;
  final String status; // pending_approval | approved | generating | ready | rejected | failed
  final String? rejectionReason;
  final String? errorMessage;
  final String? summaryText;
  final String? introVideoUrl;
  final int estimatedChars;
  final double estimatedCostUsd;
  final String createdAt;
  final String? updatedAt;

  AvatarLecture({
    required this.id,
    required this.avatarId,
    required this.classId,
    required this.createdBy,
    required this.title,
    this.sourceFilename,
    required this.durationMinutes,
    required this.style,
    required this.autoSummary,
    required this.status,
    this.rejectionReason,
    this.errorMessage,
    this.summaryText,
    this.introVideoUrl,
    required this.estimatedChars,
    required this.estimatedCostUsd,
    required this.createdAt,
    this.updatedAt,
  });

  bool get isReady => status == 'ready';
  bool get isPendingApproval => status == 'pending_approval';
  bool get isGenerating => status == 'approved' || status == 'generating';
  bool get isRejected => status == 'rejected';
  bool get isFailed => status == 'failed';
  bool get canDelete => isPendingApproval || isRejected || isFailed;

  factory AvatarLecture.fromJson(Map<String, dynamic> json, ApiService api) {
    return AvatarLecture(
      id: (json['id'] as num).toInt(),
      avatarId: (json['avatar_id'] as num?)?.toInt() ?? 0,
      classId: (json['class_id'] as num?)?.toInt() ?? 0,
      createdBy: (json['created_by'] as num?)?.toInt() ?? 0,
      title: json['title'] as String? ?? '',
      sourceFilename: json['source_filename'] as String?,
      durationMinutes: (json['duration_minutes'] as num?)?.toInt() ?? 0,
      style: json['style'] as String? ?? 'university',
      autoSummary: json['auto_summary'] as bool? ?? true,
      status: json['status'] as String? ?? 'pending_approval',
      rejectionReason: json['rejection_reason'] as String?,
      errorMessage: json['error_message'] as String?,
      summaryText: json['summary_text'] as String?,
      introVideoUrl: (json['intro_video_url'] == null || json['intro_video_url'].toString().isEmpty)
          ? null
          : api.fixUrl(json['intro_video_url'].toString()),
      estimatedChars: (json['estimated_chars'] as num?)?.toInt() ?? 0,
      estimatedCostUsd: (json['estimated_cost_usd'] as num?)?.toDouble() ?? 0.0,
      createdAt: json['created_at'] as String? ?? '',
      updatedAt: json['updated_at'] as String?,
    );
  }
}

/// A single slide within a ready avatar lecture: image, narration text and audio.
