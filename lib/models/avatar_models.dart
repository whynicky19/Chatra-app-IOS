          : api.fixUrl(json['audio_url'].toString()),
      audioDurationSeconds: (json['audio_duration_seconds'] as num?)?.toDouble(),
    );
  }
}

/// A full lecture (metadata + slides), as returned by GET /avatars/lectures/{id}/full.
class AvatarLectureFull {
  final AvatarLecture lecture;
  final List<AvatarLectureSlide> slides;
  // The teacher's avatar photo, shown in the speaking-avatar circle. The
  // backend may embed it a few different ways depending on the endpoint
  // version, so we try several fallbacks and accept null (falls back to a
  // placeholder icon in the UI).
  final String? avatarPhotoUrl;

  AvatarLectureFull({required this.lecture, required this.slides, this.avatarPhotoUrl});

  factory AvatarLectureFull.fromJson(Map<String, dynamic> json, ApiService api) {
    final rawSlides = json['slides'];
    String? photo;
    final rawAvatar = json['avatar'];
    if (rawAvatar is Map && rawAvatar['photo_url'] != null) {
      photo = rawAvatar['photo_url'].toString();
    } else if (json['avatar_photo_url'] != null) {
      photo = json['avatar_photo_url'].toString();
    } else if (json['teacher_avatar_photo_url'] != null) {
      photo = json['teacher_avatar_photo_url'].toString();
    }
    return AvatarLectureFull(
      lecture: AvatarLecture.fromJson(json, api),
      slides: rawSlides is List
          ? rawSlides.map((s) => AvatarLectureSlide.fromJson(s as Map<String, dynamic>, api)).toList()
          : [],
      avatarPhotoUrl: (photo == null || photo.isEmpty) ? null : api.fixUrl(photo),
    );
  }
}
