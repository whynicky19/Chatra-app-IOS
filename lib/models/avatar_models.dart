    required this.id,
    required this.slideIndex,
    this.slideImageUrl,
    this.narrationText,
    this.audioUrl,
    this.audioDurationSeconds,
  });

  factory AvatarLectureSlide.fromJson(Map<String, dynamic> json, ApiService api) {
    return AvatarLectureSlide(
      id: (json['id'] as num).toInt(),
      slideIndex: (json['slide_index'] as num?)?.toInt() ?? 0,
      slideImageUrl: (json['slide_image_url'] == null || json['slide_image_url'].toString().isEmpty)
          ? null
          : api.fixUrl(json['slide_image_url'].toString()),
      narrationText: json['narration_text'] as String?,
